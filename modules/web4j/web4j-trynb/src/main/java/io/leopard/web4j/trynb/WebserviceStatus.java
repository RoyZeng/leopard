package io.leopard.web4j.trynb;

public class WebserviceStatus {

	private int errno;
	private String error;

	public int getErrno() {
		return errno;
	}

	public void setErrno(int errno) {
		this.errno = errno;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}

}
